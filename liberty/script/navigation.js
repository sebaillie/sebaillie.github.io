function goToHome() {
	var homeTab = document.getElementById('homeTab');
	var inventoryTab = document.getElementById('inventoryTab');
	var mapTab = document.getElementById('mapTab');
	var configTab = document.getElementById('configTab');
	var saloon = document.getElementById('saloon');
	var stables = document.getElementById('stables');
	var mine = document.getElementById('mine');
	var river = document.getElementById('river');
	var arena = document.getElementById('arena');

	homeTab.style.display = 'block';
	inventoryTab.style.display = 'none';
	mapTab.style.display = 'none';
	configTab.style.display = 'none';
	saloon.style.display = 'none';
	stables.style.display = 'none';
	mine.style.display = 'none';
	river.style.display = 'none';
	arena.style.display = 'none';
}

